	.text

#------------------------------------------------------------------
main:
		
#--------------------Fraction a------------------------------------

		addi	$a0, $0, 3	#Fraction a = new Fraction(a0,a1)
		addi 	$a1, $0, 8	# 
	#-----------------------------------------------------------
		sw	$ra, 0($sp)
		addi	$sp, $sp, -4	#push ra
	#-----------------------------------------------------------
	
		jal Fraction

	#------------------------------------------------------------
		addi	$sp, $sp, 4
		lw	$ra, 0($sp)	#pop ra
	#------------------------------------------------------------
		add	$s0, $0, $v0	#s0 holds a address

#--------------------Fraction b--------------------------------------

	addi	$a0, $0, 1	#Fraction b = new Fraction(a0,a1)
	addi 	$a1, $0, 2	# 
	

	#-----------------------------------------------------------
		sw	$ra, 0($sp)
		addi	$sp, $sp, -4	#push ra
	#-----------------------------------------------------------
	
		jal Fraction

	#------------------------------------------------------------
		addi	$sp, $sp, 4
		lw	$ra, 0($sp)	#pop ra
	#------------------------------------------------------------

	add	$s1, $0, $v0	#s1, holds b address

#------------------------Add a and b----------------------------------

	add	$a0, $0, $s0
	add 	$a1, $0, $s1
	
	#-----------------------------------------------------------
		sw	$ra, 0($sp)
		addi	$sp, $sp, -4	#push ra
	#-----------------------------------------------------------
	
		jal subtract

	#------------------------------------------------------------
		addi	$sp, $sp, 4
		lw	$ra, 0($sp)	#pop ra
	#------------------------------------------------------------


#----------------------printing Numerator----------------------------
		add	$a0, $s0, $0	#a0 = fraction a
	#-----------------------------------------------------------
		sw	$ra, 0($sp)
		addi	$sp, $sp, -4	#push ra
	#-----------------------------------------------------------
	
		jal getNumerator

		add $a0, $v0, $0
		addi $v0 $0, 1
		syscall
	#------------------------------------------------------------
		addi	$sp, $sp, 4
		lw	$ra, 0($sp)	#pop ra
	#------------------------------------------------------------

	
###################-new line-##########################################
		addi    $a0, $0, '/'		
		addi    $v0, $0, 11		
		syscall				
#####################################################################

#-----------------printing Denominator-------------------------------

		add	$a0, $s0, $0	#a0 = fraction a

	#-----------------------------------------------------------
		sw	$ra, 0($sp)
		addi	$sp, $sp, -4	#push ra
	#-----------------------------------------------------------
		jal getDenominator
	
		add $a0, $v0, $0
		addi $v0 $0, 1
		syscall
	#------------------------------------------------------------
		addi	$sp, $sp, 4
		lw	$ra, 0($sp)	#pop ra
	#------------------------------------------------------------

	
	jr $ra
	
